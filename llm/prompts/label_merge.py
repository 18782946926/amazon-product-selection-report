"""LabelMerger prompt：把语义高度重叠的 form_label 合并。

输入是去重后的 form_label 列表（视觉 LLM per-ASIN 输出的形态标签）。
轻量任务：只看标签字符串语义判合并，不看 ASIN/图。
"""
from __future__ import annotations

_PROMPT_VERSION = "lm-v1-2026-05-08"


def build_messages(labels: list[str]) -> list[dict]:
    """构建标签合并 LLM 的 messages。labels 已去重排序。"""
    n = len(labels)
    rows_text = "\n".join(f"  {i+1}. {lab}" for i, lab in enumerate(labels))

    text = (
        f"你是产品形态标签的语义分析师。下面是 {n} 个 2-6 字的形态标签（来自不同产品的视觉判别）。\n\n"
        "你的任务：找出**语义高度重叠应合并**的标签对，给出 `from→to` 合并 mapping。\n\n"
        "**关键原则（跨品类通用）**：\n\n"
        "1) **动作/姿态特征**多有重叠，优先合并：\n"
        "   - 手持式 / 便携式 / 手提式 / 可携带 / 桌面式 / 立式 → 这些常常指代同一产品形态，应合并\n"
        "   - 例：「手持式」与「便携式」语义重叠（凡便携必能手持）→ 合并到一个（如统一为「手持式」）\n\n"
        "2) **装配/连接特征**语义独立，**不合并**：\n"
        "   - 磁吸式 / 夹式 / 挂钩式 / 支架式 / 三脚架式 / 壁挂式 / 吸盘式 / 卡扣式 / 插头式 / 头戴式\n"
        "   - 这些指代具体的物理装配方式，二值清晰（要么有磁吸底座要么没有）→ 保留独立\n\n"
        "3) **几何形态**独立，不合并：\n"
        "   - 立方体 / 圆筒 / 球形 / 多边形 / 长条形 → 形状不同则保留独立\n\n"
        "4) **品类专属功能形态**独立，不合并：\n"
        "   - ATM式 / 礼盒式 / 卡通造型 / 字母 / 多格 / 喷枪式 / 探照式 / 工作灯式\n"
        "   - 这些是该品类的核心细分维度，不能合并\n\n"
        "5) **疑义则保留**——少合并好过错合并。如果你不确定两个标签是否真重叠，**不要写进 mapping**。\n\n"
        "**合并方向**：把更具体的 / 修饰性的标签合并到更通用的。\n"
        "  例：{「便携式」: 「手持式」} （便携是手持的修饰，归到通用的手持）\n"
        "       {「手提式」: 「手持式」} （手提是手持的子类）\n\n"
        f"**待分析标签（共 {n} 个）**：\n{rows_text}\n\n"
        "只输出 JSON 对象（不需要的字段不写，没有要合并的就给空 mapping）：\n"
        "{\n"
        '  "merge_mapping": {\n'
        '    "便携式": "手持式",\n'
        '    "手提式": "手持式"\n'
        "  }\n"
        "}\n\n"
        "如果所有标签都应保留独立，输出：`{\"merge_mapping\": {}}`"
    )
    return [{"role": "user", "content": text}]
